package com.jamieday;

public abstract class Animal {


    private int weight;
    private int height;




    public void setWeight(int newWeight){

        weight= newWeight;

    }

    public int getWeight(){

        return weight;

    }

    public void setHeight(int newHeight){

        height= newHeight;

    }

    public int getHeight(){

        return height;

    }




}